package basic;

public class Variable {
	
	public static void main(String[] arg) {
		
		// create a variable
		int age;
		String name;
		float height;
		// assign values
		
		age = 23;
		name = "Sahil";
		height = 5.9f;
		
		System.out.println("Your Name is : "+name);
		System.out.println("Your Age is : "+age);
		System.out.println("Your Height is : "+height);
		
		
	}

}
