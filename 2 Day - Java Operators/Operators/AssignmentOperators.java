

public class AssignmentOperators {
    public static void main(String[] args) {

        int num = 10;
        // num = num + 10
        num += 10;
        System.out.println(num);
        num -= 10;
        System.out.println(num);
        num /= 10;
        System.out.println(num);
        num *= 10;
        System.out.println(num);


    }
}
